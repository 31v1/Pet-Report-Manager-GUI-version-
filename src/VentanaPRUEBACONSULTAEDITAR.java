    private void CambiarTipoReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CambiarTipoReporteActionPerformed
         try {
        String idReporte = txtIdReporte.getText().trim();
        
        if (idReporte.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Primero cargue un reporte", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 1. Obtener tipo actual del reporte cargado
        GestorArchivos gestorArchivos = new GestorArchivos();
        ArrayList<ReporteMascota> todos = gestorArchivos.cargarTodosLosReportes();
        GestorReportes gestorReportes = new GestorReportes();
        gestorReportes.setListaReportes(todos);
        
        ReporteMascota reporte = gestorReportes.buscarReportePorId(idReporte);
        if (reporte == null) {
            JOptionPane.showMessageDialog(this, "Reporte no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String tipoActual = reporte.getTipoReporte();
        String nuevoTipo = tipoActual.equals("PDR") ? "ENC" : "PDR";

        // 2. Confirmar con el usuario
        int confirmacion = JOptionPane.showConfirmDialog(this, 
            "¿Cambiar reporte de " + tipoActual + " a " + nuevoTipo + "?",
            "Confirmar cambio", 
            JOptionPane.YES_NO_OPTION);
        
        if (confirmacion != JOptionPane.YES_OPTION) {
            return;
        }

        // 3. Usar tu método joya
        boolean exito = gestorArchivos.moverReporteEntreArchivos(idReporte, nuevoTipo);
        
        if (exito) {
            JOptionPane.showMessageDialog(this, 
                "✅ Reporte cambiado de " + tipoActual + " a " + nuevoTipo, 
                "Éxito", JOptionPane.INFORMATION_MESSAGE);
            
            // Opcional: recargar datos
            btnCargarDatosActionPerformed(null);
        } else {
            JOptionPane.showMessageDialog(this, 
                "❌ Error al cambiar tipo", 
                "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_CambiarTipoReporteActionPerformed

