    private void btnBuscarReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarReporteActionPerformed
        try {
            //Aqui se leen el criterio y valor
            String criterio = cmbBuscarPor.getSelectedItem().toString();
            String valor = txtValorBusqueda.getText();
            
            //Validacion especie
            if ("Especie".equals(criterio)){
                Mascota mascotaTest = new Perro();
                mascotaTest.setEspecie(valor);
            }
            
            if ("Cédula".equals(criterio)){
                Cliente clienteTest = new Cliente();
                clienteTest.setIdReportante(valor);
            }

            // Cargar los datos y crear GestorReportes
            GestorArchivos gestor = new GestorArchivos();
            ArrayList<ReporteMascota> todosLosReportes = gestor.cargarTodosLosReportes();

            GestorReportes gestorReportes = new GestorReportes();
            gestorReportes.setListaReportes(todosLosReportes);

            //Ejecutar la busqueda
            ArrayList<ReporteMascota> resultados = gestorReportes.consultarPorCriterio(criterio, valor);

            //Modelo de la tabla
            String [] columnasTipo ={"Id Reporte", "Tipo de reporte","Fecha","Zona","Cédula", "Nombre Completo", "Teléfono", 
                "Especie", "Nombre", "Color", "Microchip", "Señas particulares", "Raza", "Talla", "Tipo Pelaje", "Collar", "Esterilizado"};
            DefaultTableModel modelTipo = new DefaultTableModel(columnasTipo, 0);

            //Llenando la tabla con los datos

            for (ReporteMascota reporte : resultados) {
                String raza = "";
                String talla = "";
                String tipoPelaje = ""; 
                String collarStr = "";
                String esterilizadoStr = "";

                if (reporte.getMascota() instanceof Perro) {
                    Perro perro = (Perro) reporte.getMascota();
                    raza = perro.getRaza();
                    talla = perro.getTalla();
                    collarStr = perro.isTieneCollar()  ? "Sí":"No" ;
                } else if (reporte.getMascota() instanceof Gato){
                    Gato gato = (Gato) reporte.getMascota();
                    raza = gato.getRaza();
                    tipoPelaje = gato.getTipoPelaje();
                    esterilizadoStr = gato.isEsterilizado()  ? "Sí":"No" ;
                }
                    
                modelTipo.addRow (new Object[] {
                    reporte.getIdReporte(),
                    reporte.getTipoReporte(),
                    reporte.getFechaFormateada(),
                    reporte.getZonaReporte(),
                    reporte.getCliente().getIdReportante(),
                    reporte.getCliente().getNombreCompleto(),
                    reporte.getCliente().getTelefonoContacto(),
                    reporte.getMascota().getEspecie(),
                    reporte.getMascota().getNombre(),
                    reporte.getMascota().getColorPrincipal(),
                    reporte.getMascota().getMicrochip(),
                    reporte.getMascota().getSeniasParticulares(),
                    raza, talla, tipoPelaje, collarStr, esterilizadoStr                     
                });
            }

            //Asignar los datos a la tabla
            tblConsultaReporteTipo.setModel(modelTipo);

            //Mensajes de confirmacion
            JOptionPane.showMessageDialog(this, "Datos cargados exitosamente: " + resultados.size() + " reportes", "Información cargada", JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar la información: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarReporteActionPerformed

