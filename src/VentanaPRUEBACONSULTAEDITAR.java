    private void btnGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCambiosActionPerformed
                                                     
    try {
        String idReporte = txtIdReporte.getText().trim();
        
        if (idReporte.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Primero cargue un reporte", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 1. CARGAR REPORTE ORIGINAL
        GestorArchivos gestorArchivos = new GestorArchivos();
        ArrayList<ReporteMascota> todos = gestorArchivos.cargarTodosLosReportes();
        GestorReportes gestorReportes = new GestorReportes();
        gestorReportes.setListaReportes(todos);
        
        ReporteMascota reporteOriginal = gestorReportes.buscarReportePorId(idReporte);
        if (reporteOriginal == null) {
            JOptionPane.showMessageDialog(this, "Primero cargue un reporte", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 2. CREAR OBJETOS CON DATOS ACTUALES
        Cliente clienteEditado = new Cliente(
            txtNombreCompleto.getText().trim(),
            reporteOriginal.getCliente().getIdReportante(),
            txtTelefonoContacto.getText().trim()
        );

        Mascota mascotaEditada;
        if (reporteOriginal.getMascota() instanceof Perro) {
            Perro perroOriginal = (Perro) reporteOriginal.getMascota();
            mascotaEditada = new Perro(
                perroOriginal.getEspecie(),
                txtColorPrincipal.getText().trim(),
                txtSeniasParticulares.getText().trim(),
                txtMicrochip.getText().trim(),
                txtNombre.getText().trim(),
                txtRaza.getText().trim(),
                cmbTalla.getSelectedItem().toString(),
                chkTieneCollar.isSelected()
            );
        } else {
            Gato gatoOriginal = (Gato) reporteOriginal.getMascota();
            mascotaEditada = new Gato(
                gatoOriginal.getEspecie(),
                txtColorPrincipal.getText().trim(),
                txtSeniasParticulares.getText().trim(),
                txtMicrochip.getText().trim(),
                txtNombre.getText().trim(),
                txtRaza.getText().trim(),
                cmbTipoPelaje.getSelectedItem().toString(),
                chkEsterilizado.isSelected()
            );
        }

        // 3. CREAR REPORTE TEMPORAL
        ReporteMascota nuevosDatos;
        if (reporteOriginal.getTipoReporte().equals("PDR")) {
            nuevosDatos = new ReportePerdida(
                idReporte,
                reporteOriginal.getFechaFormateada(),
                false,
                txtZonaReporte.getText().trim(),
                clienteEditado,
                mascotaEditada
            );
        } else {
            nuevosDatos = new ReporteEncontrada(
                idReporte,
                reporteOriginal.getFechaFormateada(),
                false,
                txtZonaReporte.getText().trim(),
                clienteEditado,
                mascotaEditada
            );
        }

        // 🆕 4. GUARDAR DIRECTAMENTE EN ARCHIVO (sin usar GestorReportes)
        gestorArchivos.eliminarReporte(idReporte);      // Elimina el viejo
        gestorArchivos.guardarReporte(nuevosDatos);     // Guarda el nuevo

        JOptionPane.showMessageDialog(this, "Cambios guardados exitosamente", "Éxito", JOptionPane.INFORMATION_MESSAGE);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnGuardarCambiosActionPerformed

